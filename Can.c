/* Macro to define Interrupt Service Routine.
 * This macro:
 * - defines linker section as .intvec_tc<vector number>_<interrupt priority>.
 * - defines compiler specific attribute for the interrupt functions.
 * - defines the Interrupt service routine as ISR function.
 *
 * IFX_INTERRUPT(isr, vectabNum, priority)
 *  - isr: Name of the ISR function.
 *  - vectabNum: Vector table number.
 *  - priority: Interrupt priority. Refer Usage of Interrupt Macro for more details.
 */

IFX_INTERRUPT(canIsrRxHandler, 0, ISR_PRIORITY_CAN_RX);

/* Interrupt Service Routine (ISR) called once the TX interrupt has been generated.
 * Turns on the LED1 to indicate successful CAN message transmission.
 */


/* Interrupt Service Routine (ISR) called once the RX interrupt has been generated.
 * Compares the content of the received CAN message with the content of the transmitted CAN message
 * and in case of success, turns on the LED2 to indicate successful CAN message reception.
 */
void canIsrRxHandler(void)
{
    IfxMultican_Status readStatus;

    /* Read the received CAN message and store the status of the operation */
    readStatus = IfxMultican_Can_MsgObj_readMessage(&MultiCanNode0.canDstMsgObj, &MultiCanNode0.rxMsg);

    /* If no new data has been received, report an error */
    if( !( readStatus & IfxMultican_Status_newData ) )
    {
        while(1)
        {
        }
    }

    /* If new data has been received but with one message lost, report an error */
    if( readStatus == IfxMultican_Status_newDataButOneLost )
    {
        while(1)
        {
        }
    }

    /* Finally, check if the received data matches with the transmitted one */
    if( ( MultiCanNode0.rxMsg.data[0] == MultiCanNode0.txMsg.data[0] ) &&
        ( MultiCanNode0.rxMsg.data[1] == MultiCanNode0.txMsg.data[1] ) &&
        ( MultiCanNode0.rxMsg.id == MultiCanNode0.txMsg.id ) )
    {
    }
}

/* Function to initialize MULTICAN module, nodes and message objects related for this application use case */
void Can_InitNode0(void)
{
    /* ==========================================================================================
     * CAN module configuration and initialization:
     * ==========================================================================================
     *  - load default CAN module configuration into configuration structure
     *
     *  - define the interrupt priority for both interrupt node pointers used in the example
     *
     *  - initialize CAN module with the modified configuration
     * ==========================================================================================
     */
    IfxMultican_Can_initModuleConfig(&MultiCanNode0.canConfig, &MODULE_CAN);

    MultiCanNode0.canConfig.nodePointer[RX_INTERRUPT_SRC_ID].priority = ISR_PRIORITY_CAN_RX;

    IfxMultican_Can_initModule(&MultiCanNode0.can, &MultiCanNode0.canConfig);

    setPinOutput(&MODULE_P20, 6);
    setPinOutput(&MODULE_P00,5);

    /* ==========================================================================================
     * Source CAN node configuration and initialization:
     * ==========================================================================================
     *  - load default CAN node configuration into configuration structure
     *
     *  - set source CAN node in the "Loop-Back" mode (no external pins will be used)
     *  - assign source CAN node to CAN node 0
     *
     *  - initialize the source CAN node with the modified configuration
     * ==========================================================================================
     */
    IfxMultican_Can_Node_initConfig(&MultiCanNode0.canNodeConfig, &MultiCanNode0.can);

    MultiCanNode0.canNodeConfig.nodeId = IfxMultican_NodeId_0;
    MultiCanNode0.canNodeConfig.rxPin = &IfxMultican_RXD0B_P20_7_IN;
    MultiCanNode0.canNodeConfig.rxPinMode = IfxPort_InputMode_pullUp;
    MultiCanNode0.canNodeConfig.txPin = &IfxMultican_TXD0_P20_8_OUT;
    MultiCanNode0.canNodeConfig.txPinMode = IfxPort_OutputMode_pushPull;
    MultiCanNode0.canNodeConfig.pinDriver = IfxPort_PadDriver_cmosAutomotiveSpeed3;
    MultiCanNode0.canNodeConfig.baudrate = 500000;
    IfxMultican_Can_Node_init(&MultiCanNode0.canSrcNode, &MultiCanNode0.canNodeConfig);
}


void Can_InitTransmitMsgObj(uint32 CanId)
{
  IfxMultican_Can_MsgObj_initConfig(&MultiCanNode0.canMsgObjConfig, &MultiCanNode0.canSrcNode);

  MultiCanNode0.canMsgObjConfig.msgObjId = SRC_MESSAGE_OBJECT_ID;
  MultiCanNode0.canMsgObjConfig.messageId = CanId;
  MultiCanNode0.canMsgObjConfig.frame = IfxMultican_Frame_transmit;
  MultiCanNode0.canMsgObjConfig.node = &MultiCanNode0.canSrcNode;
  MultiCanNode0.canMsgObjConfig.msgObjCount = 1;
  MultiCanNode0.canMsgObjConfig.control.messageLen = IfxMultican_DataLengthCode_8;
  MultiCanNode0.canMsgObjConfig.acceptanceMask = 0xFF;
  MultiCanNode0.canMsgObjConfig.control.extendedFrame = FALSE;
  MultiCanNode0.canMsgObjConfig.control.matchingId = TRUE;

  IfxMultican_Can_MsgObj_init(&MultiCanNode0.canSrcMsgObj, &MultiCanNode0.canMsgObjConfig);
}

void Can_InitReceiveMsgObj(uint32 CanId)
{
  MultiCanNode0.canMsgObjConfig.msgObjId = DST_MESSAGE_OBJECT_ID;
  MultiCanNode0.canMsgObjConfig.messageId = CanId;
  MultiCanNode0.canMsgObjConfig.frame = IfxMultican_Frame_receive;
  MultiCanNode0.canMsgObjConfig.node = &MultiCanNode0.canSrcNode;
  MultiCanNode0.canMsgObjConfig.msgObjCount = 1;
  MultiCanNode0.canMsgObjConfig.control.messageLen = IfxMultican_DataLengthCode_8;
  MultiCanNode0.canMsgObjConfig.acceptanceMask = 0x0;
  MultiCanNode0.canMsgObjConfig.control.extendedFrame = FALSE;
  MultiCanNode0.canMsgObjConfig.control.matchingId = TRUE;
  MultiCanNode0.canMsgObjConfig.rxInterrupt.enabled = TRUE;
  MultiCanNode0.canMsgObjConfig.rxInterrupt.srcId = RX_INTERRUPT_SRC_ID;

  IfxMultican_Can_MsgObj_init(&MultiCanNode0.canDstMsgObj, &MultiCanNode0.canMsgObjConfig);

  /* Invalidation of the RX message */
  IfxMultican_Message_init(&MultiCanNode0.rxMsg,
                           INVALID_ID_VALUE,
                           INVALID_DATA_VALUE,
                           INVALID_DATA_VALUE,
                           MultiCanNode0.canMsgObjConfig.control.messageLen);
}


/* Function to initialize both TX and RX messages with the default data values.
 * After initialization of the messages, the TX message will be transmitted.
 */
void transmitCanMessage(uint32 dataLow, uint32 dataHigh)
{
    togglePinOutput(&MODULE_P00,5);
    /* Initialization of the TX message */
    IfxMultican_Message_init(&MultiCanNode0.txMsg,
                             MultiCanNode0.canMsgObjConfig.messageId,
                             dataLow,
                             dataHigh,
                             MultiCanNode0.canMsgObjConfig.control.messageLen);

    /* Send the CAN message with the previously defined TX message content */

    while (  IfxMultican_Status_notSentBusy ==  IfxMultican_Can_MsgObj_sendMessage(&MultiCanNode0.canSrcMsgObj, &MultiCanNode0.txMsg) )
    {

    }
}
