IFX_INTERRUPT(InterruptHandlerAru, 0, ISR_PRIORITY_ARU);

void InterruptHandlerAru(void)
{
  IsrReady = TRUE;
  /* Deactivate the interrupt */
  GTM_ARU_IRQ_NOTIFY.U = 0x4;
  /* Deactivate write */
  GTM_ARU_ARU_ACCESS.B.WREQ = 0;
  /* Deactivate read */
  GTM_ARU_ARU_ACCESS.B.RREQ = 0;
}


static inline void setAtomSerialConfig(IfxGtm_Atom_ToutMap* pin,Ifx_GTM *GTM, uint16 clock, uint32 startingDataStream)
{
  atomSerialConfig.base.frequency                  = 100;
  atomSerialConfig.base.isrPriority                = 0;
  atomSerialConfig.base.isrProvider                = IfxSrc_Tos_cpu0;
  atomSerialConfig.base.minResolution              = 0;
  atomSerialConfig.base.trigger.outputMode         = IfxPort_OutputMode_pushPull;
  atomSerialConfig.base.trigger.outputDriver       = IfxPort_PadDriver_cmosAutomotiveSpeed1;
  atomSerialConfig.base.trigger.risingEdgeAtPeriod = Ifx_ActiveState_low;
  atomSerialConfig.base.trigger.outputEnabled      = TRUE;
  atomSerialConfig.base.trigger.enabled            = TRUE;
  atomSerialConfig.base.trigger.triggerPoint       = startingDataStream;
  atomSerialConfig.base.trigger.isrPriority        = 0;
  atomSerialConfig.base.trigger.isrProvider        = IfxSrc_Tos_cpu0;
  atomSerialConfig.base.startOffset                = 0.0;
  atomSerialConfig.gtm            = GTM;
  atomSerialConfig.atom           = pin->atom;
  atomSerialConfig.timerChannel   = pin->channel;
  atomSerialConfig.triggerOut     = pin;
  atomSerialConfig.clock          = clock;
  atomSerialConfig.base.countDir  = IfxStdIf_Timer_CountDir_up;
  atomSerialConfig.irqModeTimer   = IfxGtm_IrqMode_level;
  atomSerialConfig.irqModeTrigger = IfxGtm_IrqMode_pulseNotify;
  atomSerialConfig.initPins       = TRUE;
}

void AtomSerialPwm_Init(IfxGtm_Atom_Timer* mytimer,float32 frequency, uint32 startingDataStream, uint32 bitShift, IfxGtm_Atom_ToutMap* atomPin)
{
   IfxGtm_enable(&MODULE_GTM);
   /* Note the clock index should be different than 0 in order to have fixed frequency */
   IfxGtm_Cmu_setClkFrequency(&MODULE_GTM, IfxGtm_Cmu_Clk_5, frequency);
   IfxGtm_Cmu_enableClocks(&MODULE_GTM, IFXGTM_CMU_CLKEN_CLK5);

   setAtomSerialConfig(atomPin, &MODULE_GTM, IfxGtm_Cmu_Clk_5, startingDataStream);

   IfxGtm_Atom_TimerSerial_init(mytimer, &atomSerialConfig, bitShift, IfxGtm_Atom_AruInput_disabled);
   IfxGtm_Atom_Timer_run(mytimer);
}


void AtomAru_Init(IfxGtm_Atom_Timer* mytimer,float32 frequency, uint32 startingDataStream, uint32 bitShift, IfxGtm_Atom_ToutMap* atomPin, uint32 address, uint16* data)
{
  IfxGtm_enable(&MODULE_GTM);
  /* Note the clock index should be different than 0 in order to have fixed frequency */
  IfxGtm_Cmu_setClkFrequency(&MODULE_GTM, IfxGtm_Cmu_Clk_5, frequency);
  IfxGtm_Cmu_enableClocks(&MODULE_GTM, IFXGTM_CMU_CLKEN_CLK5);

  setAtomSerialConfig(atomPin, &MODULE_GTM, IfxGtm_Cmu_Clk_5, startingDataStream);

  IfxGtm_Atom_TimerSerial_init(mytimer, &atomSerialConfig, bitShift, IfxGtm_Atom_AruInput_enabled);
  IfxGtm_Atom_Timer_stop(mytimer);
  IfxGtm_Atom_Ch_setAruReadAddress0(mytimer->atom, mytimer->triggerChannel, address);
  IfxGtm_Atom_Timer_run(mytimer);

  /* Enable the automatic update of the atom timer */
  IfxGtm_Atom_Timer_applyUpdate(mytimer);
  /* GTM ARU INITIALIZATION */
  GTM_ARU_ARU_ACCESS.U = 0x00000000;
  GTM_ARU_DATA_H.U = data[1];
  GTM_ARU_DATA_L.U = data[0];
  GTM_ARU_ARU_ACCESS.B.ADDR  = address;

  GTM_ARU_IRQ_EN.U = 0x0;

  /* Pulse Notify Mode */
  GTM_ARU_IRQ_MODE.U = IfxGtm_IrqMode_level;

  GTM_ARU_IRQ_EN.U = 0x4;

  /* Initialization of the interrupt for the ARU */
  volatile Ifx_SRC_SRCR *src;
  src = &MODULE_SRC.GTM.GTM[0].ARUIRQ[2];
  IfxSrc_init(src, IfxSrc_Tos_cpu0, ISR_PRIORITY_ARU);
  IfxSrc_enable(src);
  /* Enable Write Access */
  IsrReady = FALSE;
  GTM_ARU_ARU_ACCESS.B.WREQ = 1;
  /* Enable the automatic update of the atom timer */
  //IfxGtm_Atom_Timer_applyUpdate(mytimer);
}

void AtomAru_SendData(uint32 address, uint16* data)
{
  while (IsrReady == FALSE);
  GTM_ARU_DATA_H.U = data[1];
  GTM_ARU_DATA_L.U = data[0];
  GTM_ARU_ARU_ACCESS.B.ADDR  = address;
  GTM_ARU_ARU_ACCESS.B.WREQ = 1;
  /* Reset the IsrReady flag */
  IsrReady = FALSE;
}


void AtomAru_ReadData(uint32 address, uint16* data)
{
  GTM_ARU_ARU_ACCESS.B.ADDR  = address;
  GTM_ARU_ARU_ACCESS.B.RREQ = 1;

  /* Wait till the interrupt is finished */
  while (!(GTM_ARU_IRQ_NOTIFY.U & 0x4));

  data[0] = (uint16)GTM_ARU_DATA_L.U;
  data[1] = (uint16)GTM_ARU_DATA_H.U;
}
