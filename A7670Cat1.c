void Set_A7670eCatCmd(uint8* command,Ifx_SizeT length)
{
    for(Ifx_SizeT i=0;i<length;i++)
    {
        A7670eCatCmd[i] = command[i];
    }
}

Ifx_SizeT Get_LenA7670eCatCmd(void)
{
    Ifx_SizeT length = 0;
    for(Ifx_SizeT i=0;i<sizeof(A7670eCatCmd);i++)
    {
        if(A7670eCatCmd[i]=='\0')
        {
            return length;
        }
        else
        {
            length++;
        }
    }
    return length;
}


void vDoWaitUserCommand(void)
{

}


void vDoA7670(void)
{
    switch(eStateA7670mon)
    {
        case enWaitMode:
            vDoWaitUserCommand();
            break;

        case EnReadMode:
            if(LTEstate == enEndRead)
            {
                eStateA7670mon = enWaitMode;
            }
            else
            {
                vDoReadLTE();
            }
            break;

        case enWriteMode:
            break;

        default:
            break;
    }
}
