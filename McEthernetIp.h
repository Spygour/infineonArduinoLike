typedef struct MCETH_IP_NODE
{
    uint8* PacketMessage;
    uint8  PacketSize;
    struct ETHERNET_IP_NODE *NextNode;
}MCETH_IP_NODE;

typedef enum
{
  ETH_TRANSMIT_IDLE,
  SEND_ETHERNET_HEADER,
  SEND_IP_HEADER,
  SEND_TCP_HEADER,
  SEND_PAYLOAD,
  ETH_TRANSMIT_VALIDATE
}MCETH_IP_TRANSMIT_STATE;

typedef enum
{
  ETH_RECEIVE_IDLE,
  READ_ETHERNET_HEADER,
  READ_IP_HEADER,
  READ_TCP_HEADER,
  READ_PAYLOAD,
  ETH_RECEIVE_VALIDATE
}MCETH_IP_RECEIVE_STATE;

typedef struct
{
    uint8*        McEthIp_SrcAddress;
    uint8*        McEthIp_DstAddress;
}MCETHIP_IP;

typedef struct
{
    uint8      Version_HeaderLength;
    uint8      DifferecialServices;
    uint16     TotalLength;
    uint16     Identification;
    uint16     FragmentOffset;
    uint8      TimeToLive;
    uint8      Protocol;
    uint16     CheckSum;
    MCETHIP_IP *SrcDstIpAddress;
}MCETH_IPHEADER;

typedef struct
{
   uint16  SrcPort;
   uint16  DstPort;
   uint32  SeqNumber;
   uint32  Aknowledgment;
   uint8   DataOffset;
   uint8   Flags;
   uint16  WindowSize;
   uint16  CheckSum;
   uint16  UrgentPointer;
   uint16  Options;
}MCETH_TCPHEADER;

typedef struct
{
    MCETH_IPHEADER* IpHeader;
    MCETH_TCPHEADER* TcpHeader;
}MCETH_HEADER;


